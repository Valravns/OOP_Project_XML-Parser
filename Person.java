package CourseProject.XMLParser;

public class Person {
    private int ID;
    private String gender;
    private int age;
    private String name;
    private String address;
    private String position;

    public Person() {}

    public int getID() {
        return ID;
    }
    public String getGender() {
        return gender;
    }
    public int getAge() {
        return age;
    }
    public String getName() {
        return name;
    }
    public String getAddress() {
        return address;
    }
    public String getPosition() {
        return position;
    }

}
